#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*str;
	size_t			i;

	i = 0;
	str = b;
	if (!b)
		return (0);
	while (i < len)
	{
		str[i] = c;
		i++;
	}
	return (b);
}
